n1 = int(input('Digite um número: '))
n2 = int(input('Digite mais um número: '))
s = n1 + n2
sub = n1 - n2
m = n1 * n2
d = n1 / n2
e = n1 ** n2
di = n1 // n2
r = n1 % n2
print('A soma é {}, a subtração é {}, o produto é {}, a divisão é {}, a potência é {}, a divisão inteira é {} e o resto é {}'.format(s,sub,m,d,e,di,r))
