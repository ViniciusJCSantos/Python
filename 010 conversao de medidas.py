metros = float(input('Digite um valor em metros: '))
centímetros = metros * 100
milímetros = metros * 1000
decímetros = metros * 10
decâmetros = metros / 10
hectômetros = metros / 100
kilômetros = metros / 1000
print('{} metros equivalem a: \n{} centímetros; \n{} milímetros; \n{} decímetros; \n{} decâmetros; \n{} hectômetros; \n{} kilômetros.'.format(metros, centímetros, milímetros, decímetros, decâmetros, hectômetros, kilômetros))
